package io.fp.notebook;

public class IntelligentNoteException extends Exception {

    public IntelligentNoteException() {
    }

    public IntelligentNoteException(String arg0) {
        super(arg0);
    }

    public IntelligentNoteException(Throwable arg0) {
        super(arg0);
    }

    public IntelligentNoteException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public IntelligentNoteException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }
    
}
